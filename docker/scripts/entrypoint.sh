#!/usr/bin/env bash
# =============================================================================
# entrypoint.sh — Docker 容器啟動腳本
# 負責首次初始化檢查，然後執行使用者指定的命令（預設 zsh）
# =============================================================================

set -euo pipefail

BOOTSTRAP_DONE_FLAG="${HOME}/.arch-bootstrap-done"

# --------------------------------------------------------------------------
# 首次啟動初始化
# --------------------------------------------------------------------------
if [ ! -f "$BOOTSTRAP_DONE_FLAG" ]; then
  echo "[entrypoint] 首次啟動，執行初始化..."
  bash /usr/local/bin/bootstrap.sh
  touch "$BOOTSTRAP_DONE_FLAG"
  echo "[entrypoint] 初始化完成"
fi

# --------------------------------------------------------------------------
# 若 workspace volume 已掛載，設定正確的工作目錄
# --------------------------------------------------------------------------
if [ -d /workspace ] && [ -n "$(ls -A /workspace 2>/dev/null)" ]; then
  cd /workspace
fi

# --------------------------------------------------------------------------
# 執行傳入的命令（預設 zsh）
# --------------------------------------------------------------------------
exec "$@"
